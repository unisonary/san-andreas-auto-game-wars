using UnityEngine;

public class RearLookAt : MonoBehaviour
{
	public Transform target;

	public Vector3 position;

	private void Update()
	{
		Quaternion localRotation = Quaternion.LookRotation(base.transform.localPosition - target.localPosition + position);
		base.transform.localRotation = localRotation;
	}
}


