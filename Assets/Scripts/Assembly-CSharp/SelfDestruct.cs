using UnityEngine;

public class SelfDestruct : MonoBehaviour
{
	public float lifeTime;

	private float dieTime;

	private void Start()
	{
		dieTime = Time.time + lifeTime;
	}

	private void Update()
	{
		if (Time.time >= dieTime)
		{
			Object.Destroy(base.gameObject);
		}
	}
}


